package fr.sandrock59.teleinfo.beans;

import java.util.Date;

public class InfosConso {

	private Date date;
	private int total_hc;
	private int total_hp;
	private long daily_hc;
	private long daily_hp;
	
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public int getTotal_hc() {
		return total_hc;
	}
	public void setTotal_hc(int total_hc) {
		this.total_hc = total_hc;
	}
	public int getTotal_hp() {
		return total_hp;
	}
	public void setTotal_hp(int total_hp) {
		this.total_hp = total_hp;
	}
	public long getDaily_hc() {
		return daily_hc;
	}
	public void setDaily_hc(long daily_hc) {
		this.daily_hc = daily_hc;
	}
	public long getDaily_hp() {
		return daily_hp;
	}
	public void setDaily_hp(long daily_hp) {
		this.daily_hp = daily_hp;
	}
	
	

	
	
}
